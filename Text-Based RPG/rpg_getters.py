# Getter

def getName(self):
    return self.name

def getHealth(self):
    return self.health

def getStamina(self):
    return self.stamina

def getMagicka(self):
    return self.magicka

def getAttack(self):
    return self.attack

def getInv(self):
    return self.inventory

def getArmorRate(self):
    return self.armor